package vaqpack.model.resume;

public class ResumeStyle
{
	private String style;

	public ResumeStyle(String style)
	{
		this.style = style;
	}

	public String getStyle()
	{
		return style;
	}

	public void setStyle(String style)
	{
		this.style = style;
	}

	public String toString()
	{
		return style;
	}
}
