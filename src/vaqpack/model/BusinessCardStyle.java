package vaqpack.model;

public class BusinessCardStyle
{
	private String style;

	public BusinessCardStyle(String style)
	{
		this.style = style;
	}

	public String getStyle()
	{
		return style;
	}

	public void setStyle(String style)
	{
		this.style = style;
	}
	
	public String toString()
	{
		return style;
	}
	
}
